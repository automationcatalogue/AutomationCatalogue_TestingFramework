package teachings.java.fundamentals;

public class RelationalDemo2 {
    public static void main(String[] args) {
        int x=20, y=20;
        boolean z=x>y;

        System.out.println(z);//false
        System.out.println(x<y);//false
        System.out.println(x<=y);//true
        System.out.println(x>=y);//true
        System.out.println(x!=y);//false
        System.out.println(x==y);//true
    }
}
