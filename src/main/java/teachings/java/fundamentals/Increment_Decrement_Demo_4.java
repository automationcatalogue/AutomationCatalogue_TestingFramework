package teachings.java.fundamentals;

public class Increment_Decrement_Demo_4 {
    public static void main(String[] args) {
        int x=10;

        System.out.println(++x);

        System.out.println(x++);
        System.out.println(x);

        System.out.println(--x);

        System.out.println(x--);

    }
}
